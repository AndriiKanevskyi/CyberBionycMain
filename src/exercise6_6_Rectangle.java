public class exercise6_6_Rectangle {
    public static void main(String[] args) {

        int x=10;
        int y=20;


        for (int i = 0; i < x; i++) {
            for (int j = 0; j < y; j++) {
                System.out.print("* ");
            }
            System.out.print("\n");
        }
        System.out.println("");


    }
}
